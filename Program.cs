//Find sum of first 10 natural #s then print. 

int sum = 0;
Console.WriteLine("The first 10 natural numbers are: ");
for (int i = 1; i <= 10; i++)
{
    Console.WriteLine(i + " ");
    sum += i;
}

Console.WriteLine("\nThe sum of first 10 natural #: " + sum);
    
    
        


 